<p>Easy Quran uyarlanabilir bir web uygulamasıdır. Web sitesinden de okuyabilirsiniz, mobil cihazınıza da kurabilirsiniz.</p>
<p>İlk kullanımdan sonra internet bağlantısı olmadan da çalışır.</p>
<h4>Özellikleri</h4>
<ul class="ul">
	<li>Kolay okuma</li>
	<li>Kolay kullanım</li>
	<li>Yukarı aşağı kaydırma (Sola sağa değil)</li>
	<li>Ayet numarası ayetin başında (Sonunda değil)</li>
	<li>Webde ve mobil cihazlarda çalışma (Responsive)</li>
	<li>Kurulabilme</li>
	<li>İnternet bağlantısı olmadan kullanabilme</li>
	<li>Hafif (Yaklaşık 2MB)</li>
	<li>Süper hızlı kaydırma</li>
	<li>Sure seçimi (* - İçinde secde ayeti var.)</li>
	<li>Cüz seçimi</li>
	<li>Sayfa seçimi</li>
	<li>Çok okunan surelere kısayollar</li>
	<li>Font seçimi</li>
	<li>Punto seçimi</li>
	<li>Renk seçimi</li>
	<li>Zemin rengi seçimi</li>
	<li>Çoklu dil</li>
	<li>Cüz, sayfa ve hatta ayet yer işareti koyabilme</li>
	<li>Yazdırılabilir (Ctrl+P tuşlarına basınız.)</li>
</ul>
